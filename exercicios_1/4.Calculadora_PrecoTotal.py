# Calculadora de Preço Total
"""
Desenvolva um programa que calcule o preço total de uma compra. Use as seguintes informações:

Nome do produto: "Cadeira Infantil"
Preço unitário: R$ 12.40
Quantidade: 3 
O programa deve calcular o preço total e exibir todas as informações, incluindo o resultado final.

"""

nomeProduto = "Cadeira Infantil"
precoProduto = 12.40
quantidade = 3

precoTotal = quantidade * precoProduto

print(f"Nome do produto: {nomeProduto}")
print(f"Preço unitário: {precoProduto}")
print(f"Quantidade: {quantidade}")
print(f"Preço Total: {precoTotal}")